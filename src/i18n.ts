export const LOCALES: Record<LocaleCode, Record<StringKey, string>> = { en, fr, de, it };

export const LANGUAGE_CHOICES: Array<{ value: LanguagePreference; label: string }> = [
  { value: "auto", label: "Auto (WME)" },
  { value: "en", label: "English" },
  { value: "fr", label: "Français" },
  { value: "de", label: "Deutsch" },
  { value: "it", label: "Italiano" },
];

let current: LocaleCode = "en";

export function setLocale(code: LocaleCode): void {
  current = code;
}

export function getLocale(): LocaleCode {
  return current;
}

/** "auto" follows the WME UI locale; unsupported locales fall back to English. */
export function resolveLocale(preference: LanguagePreference, wmeLocaleCode: string): LocaleCode {
  if (preference !== "auto") return preference;
  const prefix = wmeLocaleCode.toLowerCase().slice(0, 2);
  return prefix === "fr" || prefix === "de" || prefix === "it" ? prefix : "en";
}

export function t(key: StringKey, params?: Record<string, string | number>): string {
  let s = LOCALES[current][key] ?? en[key];
  if (params) {
    for (const [name, value] of Object.entries(params)) {
      s = s.replaceAll(`{${name}}`, String(value));
    }
  }
  return s;
}
